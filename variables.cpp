#include <iostream>

using namespace std;

int main() {
    string name = "Tom";
    int age;
    age = 50;

    cout << "There once was a man named " << name << endl;
    cout << "He was " << age << " years old" << endl;

    name = "Mike";

    cout << "He liked the name " << name << endl;
    cout << "But did not like being " << age << endl;

    return 0;
}